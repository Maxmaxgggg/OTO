%                       Функция fseminf
%   Функция предназначена для нахождения решения задачи 
%   полубесконечной минимизации с ограничениями 

%% Определение целевой функции
tar_fun = @(x)sum((x-0.2).^2);

%% Поиск точки оптимума
% Стартовые значения
